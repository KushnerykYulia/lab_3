/**
 * Інтерфейс Graphic представляє певний графічний елемент, в даному випадку
 * лише коло. Інтерфейс має два методи це відображення елемента в певній точці
 * та опис елемента.
 */
public interface Graphic {

    /**
     * Відображає графічний елемент на екрані.
     */
    void display();

    /**
     * Повертає опис графічного елемента, в даному випадку кола у вигляді рядка.
     * @return опис графічного елемента
     */
    String getDescription();
}